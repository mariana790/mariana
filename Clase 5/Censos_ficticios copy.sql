
DROP DATABASE IF EXISTS Censos_económicos;
CREATE DATABASE Censos_económicos;
USE Censos_económicos;

CREATE TABLE estado (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL
);

CREATE TABLE municipio (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    estado_id INT,
    FOREIGN KEY (estado_id) REFERENCES estado(id)
);

CREATE TABLE sector_economico (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre TEXT,
    municipio_id INT,
    estado_id INT,
    FOREIGN KEY (municipio_id) REFERENCES municipio(id),
    FOREIGN KEY (estado_id) REFERENCES estado(id)
);

CREATE TABLE subsector (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(500) DEFAULT 'X',
    sector_economico_id INT,
    FOREIGN KEY (sector_economico_id) REFERENCES sector_economico(id)
);

CREATE TABLE clase (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(500) DEFAULT 'X',
    subsector_id INT,
    FOREIGN KEY (subsector_id) REFERENCES subsector(id)
);

CREATE TABLE inversion_total (
    id INT PRIMARY KEY AUTO_INCREMENT,
    inversion_total DECIMAL(15, 2)
);

CREATE TABLE produccion_bruta_total (
    id INT PRIMARY KEY AUTO_INCREMENT,
    produccion_bruta_total DECIMAL(15, 2)
);

CREATE TABLE personal_ocupado_total (
    id INT PRIMARY KEY AUTO_INCREMENT,
    personal_ocupado_total INT
);

CREATE TABLE remuneraciones (
    id INT PRIMARY KEY AUTO_INCREMENT,
    remuneraciones DECIMAL(15, 2)
);

CREATE TABLE gastos (
    id INT PRIMARY KEY AUTO_INCREMENT,
    gastos DECIMAL(15, 2)
);

CREATE TABLE ingresos (
    id INT PRIMARY KEY AUTO_INCREMENT,
    ingresos DECIMAL(15, 2)
);

CREATE TABLE activos_fijos (
    id INT PRIMARY KEY AUTO_INCREMENT,
    activos_fijos DECIMAL(15, 2)
);

CREATE TABLE empresas (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(50),
    estado_id INT,
    sector_economico_id INT,
    inversion_total_id INT,
    produccion_bruta_total_id INT,
    personal_ocupado_total_id INT,
    remuneraciones_id INT,
    gastos_id INT,
    ingresos_id INT,
    activos_fijos_id INT,
    FOREIGN KEY (estado_id) REFERENCES estado(id),
    FOREIGN KEY (sector_economico_id) REFERENCES sector_economico(id),
    FOREIGN KEY (inversion_total_id) REFERENCES inversion_total(id),
    FOREIGN KEY (produccion_bruta_total_id) REFERENCES produccion_bruta_total(id),
    FOREIGN KEY (personal_ocupado_total_id) REFERENCES personal_ocupado_total(id),
    FOREIGN KEY (remuneraciones_id) REFERENCES remuneraciones(id),
    FOREIGN KEY (gastos_id) REFERENCES gastos(id),
    FOREIGN KEY (ingresos_id) REFERENCES ingresos (id),
    FOREIGN KEY (activos_fijos_id) REFERENCES activos_fijos(id)
);

CREATE TABLE salarios (
    id INT PRIMARY KEY AUTO_INCREMENT,
    salario DECIMAL(10, 2)
);

CREATE TABLE horas_trabajadas (
    id INT PRIMARY KEY AUTO_INCREMENT,
    horas_trabajadas INT
);

CREATE TABLE empleados (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(50),
    apellido_paterno VARCHAR(50),
    apellido_materno VARCHAR(50),
    estado_id INT,
    sector_economico_id INT,
    empresas_id INT,
    salario DECIMAL(10, 2),
    horas_trabajadas INT,
    FOREIGN KEY (empresas_id) REFERENCES empresas(id)
    FOREIGN KEY (estado_id) REFERENCES estado(id),
    FOREIGN KEY (sector_economico_id) REFERENCES sector_economico(id),
);

